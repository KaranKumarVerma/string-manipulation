public class ConcatenateStrings {

    public static void main(String[] args) {
        String str1="Karan ";
        String str2="Verma";
        
        System.out.println("Concatination of String with '+' Operator:"+str1+str2);
        System.out.println("Concatination of String using concat() method:"+str1.concat(str2));
    }
    
}
